const seededAt = '2026-03-16T00:00:00.000Z'

function source(
  label: string,
  url: string,
  sourceType: Source['sourceType'],
  note: string,
  publishedAt: string | null,
): Source {
  return {
    id: createSourceId(label, url),
    label,
    url,
    sourceType,
    domain: domainFromUrl(url),
    publishedAt,
    note,
  }
}

function market(
  input: Omit<
    Market,
    | 'basePayoutMultiplier'
    | 'author'
    | 'createdAt'
    | 'updatedAt'
    | 'lastCheckedAt'
    | 'bustedAt'
    | 'betWindowOpen'
  >,
): Market {
  return {
    ...input,
    basePayoutMultiplier: input.payoutMultiplier,
    betMode: input.betMode ?? 'against_only',
    author: null,
    createdAt: seededAt,
    updatedAt: seededAt,
    lastCheckedAt: seededAt,
    bustedAt: null,
    betWindowOpen: input.status === 'open',
  }
}

